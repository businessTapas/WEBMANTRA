                    Patrika
                    ceased to be published from Kolkata office. Later in 2003, the Siliguri Edition was also closed down.
                </p>

                <p>
                    In 2007 when the Left Front Government issued a Circular to close down the Basumati Office, the then
                    opposition leader and Union Railway Minister Mamata Banerjee came forward and she started a
                    rejuvenation movement with the employees of this Office. She even sent a delegation to Basumati
                    Corporation Ltd. consisting of printing experts to suggest modalities for the revival of this
                    Office.
                </p>

                <p>
                    In 2011 The Trinomool congress led Government under the Chief Ministership of Mamata Banerjee was
                    formed. With
                    the assurance and inspiration of Mamata Banerjee, initiative has been taken to revive Basumati and to
                    recuperate the past glory of this organization.
                </p>

                <p>
                    Basumati Sahitya Mandir has been recognized as an Heritage Building in the Kolkata Municipal Corporation
                    Records.
                </p>

                <p>
                    Publication of ‘Masik’ Basumati Magazine began again from the year of 2012. Works of Reprinting and
                    publication of rare Publication books started.
                </p>

                <p>
