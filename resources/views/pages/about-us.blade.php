                    also the rst to become the subscriber of Reuter and Associated press.
                </p>

                <p>
                    With passage of time, the Dainik Basumati began tremendous attacks regularly through writings against
                    the British Government for its various anti-people laws
                </p>


                <p>
                    In 1921 Mahatma Gandhi visited the Office of the Patrika and praised it for creating awareness among the
                    youth against the British Government Rule. It also played an important role during the Satyagraha
                    Movement of Mahatma Gandhi in 1929.
                </p>

                <p>
                    Number of Spiritual and National Leaders were associated with this Patrika. Notable amongest them were—
                    Aurobindo Ghosh, Chittaranjan Das, Barin Kumar Ghosh, Upendranath Bandyopadhyaya, Sarat Chandra Basu,
                    Netaji Subhas Chandra Basu, Sarojini Naidu among others. In 1950, Barin Ghosh became the editor & served
                    in Dainik Basumati for a considerable period of time.
                </p>

                <p>
                    The popularity and demand of this Patrika reached sky-high during the British regime. The British
                    Government was reluctant to supply the required newsprint and even threatened to stop the publication
                    for criticizing the Government. An English Telegram patrika was also published in 1941 from this office.
                </p>

