                </div>
                <p>
                    After Independence, the financial condition of the Patrika began to deteriorate gradually and Basumati
                    ran into internal problems and had to close down its Office in 1961.
                </p>

                <p>
                    The former Central Law Minister Sri Asok Kumar Sen acquired the ownership of the office during this
                    period. He reopened it again as “Basumati Private Limited”.
                </p>
