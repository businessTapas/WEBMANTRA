                <p>
                    Basumati Sahitya Mandir was established in 1881 by Late Upendra Nath Mukherjee which had
                    then housed an Art College in the name of 'The Society for the Promotion of Industrial Art'. Thereafter,
                    a National College was established in this Building. Sri Aurobindo Ghosh was the Principal of this
                    College. Besides educational purposes, a number of National Leaders used to assemble here frequently to
                    evolve the plan and program for freedom of our motherland.
                </p>


                <p>
                    Upendra Nath was a close devotee and disciple of Sri Ramkrishna Paramhansadev. Later he
                    came in closed contact of Swami Vivekananda.
                </p>

                <p>A number of Patrikas were published from this building—</p>

                <ol>
                    <li>1. Bharatbarsha</li>
                    <li>2. Prabashi</li>
                    <li>3. Bichitra</li>
                    <li>4. Kisholay Patrika and Basumati (Weekly, Dainik, Fortnightly and Masik Patrika) alongside
                        several rare Publications and Books written by erudite persons.
                    </li>

                </ol>

                <p>
                    Masik Basumati was rst published in 1922. th On 6 August, 1914, immediately on the eve
                    of First world war ‘Dainik’ Basumati Patrika was launched to full the curiosity of people about the war
                    and the news of social and economic problems of the common men. “Nomo Naranarayan –Service for People
                    “—This written by Swami Vivekananda was always printed on the upper side of editorial page of this
