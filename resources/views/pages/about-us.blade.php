                <p>
                    Eminent Journalist Sri Vivekananda Mukherjee became the Editor of Dainik Basumati in 1962. Demand and
                    circulation once again began to rise. During the food movement (1962-68) under the editorship of
                    Vivekananda Mukherjee, Basumati Patrika played a memorable role which readers still do remember.
                </p>


                <p>
                    In 1974, during the period of Chief Minister Sri Siddhartha Sankar Roy, the then Congress Government
                    took over the ownership of Basumati office.
                </p>

                <p>
                    Basumati Office became Basumati Corporation Limited. Besides daily newspaper, printing of large-scale
                    jobs, execution of works of various Departments of Government were also introduced. Circulation of
                    Newspaper began to rise.
                </p>
